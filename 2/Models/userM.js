const mongoose = require("mongoose");
const userStruct = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    default: "user",
  },
});

module.exports = mongoose.model("User", userStruct);
